# Creating a list
fruits = ["apple", "banana", "orange", "grape"]

# Looping through the list
print("Printing fruits using a for loop:")
for fruit in fruits:
    print(fruit)

# Adding an element to the list
fruits.append("watermelon")

# Looping through the updated list
print("\nPrinting updated fruits using a for loop:")
for fruit in fruits:
    print(fruit)
